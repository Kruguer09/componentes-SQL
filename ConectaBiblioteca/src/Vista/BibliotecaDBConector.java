    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(!conectado){
            JOptionPane.showMessageDialog(null, "Debes conectare a la base de datos para acceder al contenido", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }else{
            DefaultTableModel modeloLibros = conexion.CargatablaLibros(tablaLibros);
            DefaultTableModel modeloPrestamos = conexion.CargatablaPrestamos(tablaPrestamos);
             tablaLibros.setModel(modeloLibros);
             tablaPrestamos.setModel(modeloPrestamos);
             int totalLibros=tablaLibros.getRowCount();
             int totalPrestamos=tablaPrestamos.getRowCount();
             labelTotLib.setText("Total de libros: "+totalLibros);
             labelTotPres.setText("Total de préstamos: "+totalPrestamos);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

