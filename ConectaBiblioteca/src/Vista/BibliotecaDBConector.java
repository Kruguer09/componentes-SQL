    private void btConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConectarActionPerformed
        String username = showInputDialog("Usuario:");
        System.out.println(username);
        if (username != null) {
            String password = showPasswordDialog("Contraseña:");
            System.out.println(password);
            if (password != null) {
                conexion = new VerRegistros(username, password);
                conectado = conexion.ConectaDB();


            }

        }

        if (conectado == true) {
            labelEstado.setForeground(Color.green);
            labelEstado.setText("Conectado");
            JOptionPane.showMessageDialog(null, "Conexión exitosa", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            labelEstado.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Conexión fallida", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btConectarActionPerformed

